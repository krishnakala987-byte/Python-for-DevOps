# Indexing and Slicing

numbers = [10, 20, 30, 40, 50]

print("Complete List:")
print(numbers)

print("\nFirst Element:")
print(numbers[0])

print("\nLast Element:")
print(numbers[-1])

print("\nFirst Three Elements:")
print(numbers[0:3])

print("\nLast Two Elements:")
print(numbers[-2:])

print("\nMiddle Elements:")
print(numbers[1:4])